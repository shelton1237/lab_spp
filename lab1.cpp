#include <iostream> 
#include <string>    

int main() {
    // Оголошення змінної для зберігання введеного користувачем числа
    int num; 
    // Виведення на екран текстового запиту на введення числа
    std::cout << "Enter Number: "; 
    // Зчитування введеного користувачем числа
     std::cin >> num; 

    // Цикл для відображення шаблону на екрані
    for (int i = 0; i < num; ++i) {
        // Формування рядка з пробілів перед числами
        std::string spaces(num - i - 1, ' ');
        // Формування рядка із зростаючою послідовністю цифр
        std::string digits(2 * i + 1, char('0' + i));
        // Виведення на екран рядка з пробілами та числами
        std::cout << spaces << digits << std::endl;
    }

    return 0;
    }